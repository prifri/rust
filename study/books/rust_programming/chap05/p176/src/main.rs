fn main() {
    let zero: u16 = 0b0000_0000_0000_0000;
    let one: u16  = 0b0000_0000_0000_0001;
    let two: u16  = 0b0000_0000_0000_0010;

    let a: u16 = 0b1111_1111_1111_1101;
    let b: u16 = 0b1111_1111_1111_1110;
    let c: u16 = 0b1111_1111_1111_1111;

    print!("{} {} {} {} {} {}\n",
           zero, one, two, a, b, c);
}
